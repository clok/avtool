func Test_splitHeader_unsupported_cipher(t *testing.T) {
	content := []byte(`$ANSIBLE_VAULT;1.2;AES128;label
NOOP
`)
	assert.PanicsWithError(t, "unsupported cipher: AES128", func() {
		_ = splitHeader(content)
	})
}

